# Programa de reconhecimento de números iguais e diferentes 
n1 = int(input('Informe o primeiro numero inteiro:'))
n2 = int(input('Informe o segundo numero inteiro):'))
if (n1 == n2):
    Multiplica = (n1 * n2)
    print(f'Numeros iguais, resultado {Multiplica}.')
else:
    soma = (n1 + n2)
    print(f'Números diferentes, resultado {soma}.')